CREATE TABLE BOOKINGS
(BOOKING_ID INTEGER NOT NULL IDENTITY(1,1),
ADDRESS_ID INTEGER, 
ADDRESS2_ID INTEGER, 
USER_ID INTEGER, 
PACKAGE_TYPE VARCHAR(MAX), 
PACKAGE_WEIGHT FLOAT, 
ITEM_QUANTITY INTEGER, 
DELIVERY_DATE DATE, 
DELIVERY_TME TIME, 
PRIMARY KEY(BOOKING_ID),
FOREIGN KEY (ADDRESS_ID) REFERENCES ADDRESSES (ADDRESS_ID),
FOREIGN KEY (ADDRESS2_ID) REFERENCES ADDRESSES (ADDRESS_ID), 
FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID),
);